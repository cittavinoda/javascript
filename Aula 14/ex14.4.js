/*Comparação entre as Estruturas de Repetição
while, do-while e for.
*/

//Estrutura de Repetição while

var c = 1
while (c <= 5){
    console.log (c)
    c++
}

//Estrutura de Repetição do-while

var c = 1
do{
    console.log (c)
    c++
}while (c <=5)

//Estrutura de Repetição com Variável de Controle For

for(var c = 1; c <= 5; c++){
    console.log (c)
}