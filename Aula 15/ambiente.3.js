'Aumentando a quantidade de elementos de um vetor sem indicação pelo método .push(). O Java Script colocará este elemento sempre na última posição.'

let num = [5,3,9,9,8,6]
console.log(`Nosso vetor é o: ${num}`)

num.push(7)
console.log(`Nosso vetor passou a ser: ${num}`)

'Descobrindo a quantidade de elementos que tem o vetor pelo atributo .length'

console.log(`Nosso vetor tem ${num.length} posições.`)

'Identificando um valor atribuído a um elemento.'

console.log(`O valor que ocupa a posição 3 do vetor é: ${num [3]}`)